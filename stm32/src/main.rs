/// Simple example demonstrating Falcon512 signing on STM32 with pre-generated keys
///
/// This example shows:
/// 1. Loading pre-generated keys from flash memory
/// 2. Signing a message
/// 3. Verifying the signature
///
/// Key generation workflow:
/// 1. Run keygen tool on laptop: `cd keygen && cargo run --release`
/// 2. Flash keys to reserved section using flash_keys tool
/// 3. Flash and run this firmware
///
/// In a real application, you would:
/// - Use hardware RNG for signing randomness (not key generation!)
/// - Handle errors appropriately
/// - Use serial/UART for communication
#[entry]
fn main() -> ! {
    // Initialize RTT for debug output
    rtt_init_print!();
    rprintln!("=== STM32H743 Falcon512 Post-Quantum Signing Demo ===");
    rprintln!("Initializing...");

    // Get device peripherals for LED control
    let dp = pac::Peripherals::take().unwrap();
    let pwr = dp.PWR.constrain();
    let pwrcfg = pwr.freeze();
    let rcc = dp.RCC.constrain();
    let ccdr = rcc.sys_ck(100.MHz()).freeze(pwrcfg, &dp.SYSCFG);

    // Setup LED on PE3 (common on STM32H7 Nucleo boards)
    // Adjust the pin based on your board
    let gpioe = dp.GPIOE.split(ccdr.peripheral.GPIOE);
    let mut led = gpioe.pe3.into_push_pull_output();

    rprintln!("LED initialized on PE3");

    // Blink LED to show startup
    rprintln!("Blinking LED 2 times (startup)...");
    for _ in 0..2 {
        led.set_high();
        delay_ms(100);
        led.set_low();
        delay_ms(100);
    }

    // Initialize the heap allocator
    // Allocate 384KB for heap (Falcon512 signing needs substantial memory)
    // Total RAM: 512KB AXI SRAM, Stack: 64KB, leaving ~64KB for other data
    rprintln!("Setting up heap allocator (384KB)...");
    {
        use core::mem::MaybeUninit;
        const HEAP_SIZE: usize = 384 * 1024;
        static mut HEAP_MEM: [MaybeUninit<u8>; HEAP_SIZE] = [MaybeUninit::uninit(); HEAP_SIZE];
        unsafe {
            let heap_ptr = core::ptr::addr_of_mut!(HEAP_MEM);
            HEAP.init((*heap_ptr).as_mut_ptr() as usize, HEAP_SIZE)
        }
    }
    rprintln!("Heap initialized successfully");

    // Initialize RNG for signing randomness ONLY
    // NOTE: In production, use hardware RNG from your STM32 chip!
    // Example: let mut rng = stm32h7xx_hal::rng::Rng::new(device.RNG, &clocks);
    rprintln!("Initializing RNG (ChaCha20 - deterministic for testing)...");
    let seed = [0x42u8; 32]; // DO NOT use fixed seed in production!
    let mut rng = ChaCha20Rng::from_seed(seed);
    rprintln!("RNG initialized");

    // Load pre-generated keys from hardcoded arrays
    // These keys are embedded directly in the firmware
    rprintln!("Loading hardcoded keys...");
    let sk_bytes: &[u8] = &SK_BYTES;
    let pk_bytes: &[u8] = &PK_BYTES;

    rprintln!(
        "Keys loaded (SK: {} bytes, PK: {} bytes)",
        sk_bytes.len(),
        pk_bytes.len()
    );

    // Reconstruct keys from bytes
    rprintln!("Reconstructing secret key...");
    let secret_key = match falcon512::SecretKey::from_bytes(sk_bytes) {
        Ok(sk) => {
            rprintln!("Secret key loaded successfully");
            sk
        }
        Err(_) => {
            rprintln!("ERROR: Failed to load secret key - invalid format");
            loop {
                cortex_m::asm::nop();
            }
        }
    };

    rprintln!("Reconstructing public key...");
    let public_key = match falcon512::PublicKey::from_bytes(pk_bytes) {
        Ok(pk) => {
            rprintln!("Public key loaded successfully");
            pk
        }
        Err(_) => {
            rprintln!("ERROR: Failed to load public key - invalid format");

            loop {
                cortex_m::asm::nop();
            }
        }
    };

    // Message to sign
    let message = b"Hello from STM32 with Falcon512!";
    rprintln!(
        "\nMessage to sign: {:?}",
        core::str::from_utf8(message).unwrap()
    );

    // Blink LED rapidly to show signing is starting
    rprintln!("\n[SIGNING] Blinking LED rapidly (signing starting)...");
    for _ in 0..5 {
        led.set_high();
        delay_ms(50);
        led.set_low();
        delay_ms(50);
    }

    // Sign the message
    rprintln!("Signing message with Falcon512...");
    led.set_high(); // Turn LED on during signing
    let signature = falcon512::sign_with_rng(message, &secret_key, &mut rng);
    led.set_low(); // Turn LED off after signing
    rprintln!("Signature generated successfully");

    // Get signature size
    let sig_bytes = signature.to_bytes();
    rprintln!("Signature size: {} bytes", sig_bytes.len());

    // Verify the signature
    rprintln!("Verifying signature...");
    let is_valid = falcon512::verify(message, &signature, &public_key);

    if is_valid {
        rprintln!("✓ SUCCESS: Signature is VALID!");
        rprintln!("Post-quantum cryptography working correctly on STM32H743");

        // Blink LED 3 times slowly to show success
        rprintln!("\n[SUCCESS] Blinking LED 3 times (verification complete)...");
        for i in 1..=3 {
            rprintln!("  Blink {}/3", i);
            led.set_high();
            delay_ms(200);
            led.set_low();
            delay_ms(200);
        }
    } else {
        rprintln!("✗ FAILURE: Signature verification FAILED!");

        // Blink LED rapidly to show error
        rprintln!("\n[ERROR] Blinking LED rapidly (verification failed)...");
        for _ in 0..10 {
            led.set_high();
            delay_ms(50);
            led.set_low();
            delay_ms(50);
        }
    }

    rprintln!("\n=== Demo Complete ===");
    rprintln!("LED will blink slowly in infinite loop...");

    loop {
        // Blink LED slowly to show program is running
        led.set_high();
        delay_ms(1000);
        led.set_low();
        delay_ms(1000);
    }
}
